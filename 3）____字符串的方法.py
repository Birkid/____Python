
name = 'china'
print(len(name))                #计算字符串的长度
print(name.find('n'))           #寻找字符串中某个字符的位置
print(name.capitalize())        #字符串首个字母大写
print(name.upper())             #字符串改为大写
print(name.lower())             #字符串改为小写
print(name.isdigit())           #判断字符串是否由数字组成
print(name.isalpha())           #判断字符串是否由字母组成 https://www.runoob.com/python/att-string-isalpha.html
print(name.count('n'))          #字符中某个字符出现的次数
print(name.replace('c','p'))    #替换字符
print(name*3)                   #打印次数